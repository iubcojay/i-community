class VehicleStory {
    static VEHICLE_STATUS_RUNNING = 1;
    static VEHICLE_STATUS_STOP = 2;
    static VEHICLE_STATUS_ACCIDENT = 3;
    constructor() {
        // 车辆信息列表
        this.vehicleInfoList = vehicleInfoList;

        // 车辆id列表
        this.vehicleIdList = vehicleInfoList.map(item => item.id);

        // 车辆运动轨迹Map
        this.vehiclePositionMap = new Map();
        this.vehicleIdList.forEach(vehicleId => {
            this.vehiclePositionMap.set(vehicleId, vehicleInfoList.find(vehicleInfo => vehicleInfo.id === vehicleId).positionList)
        })

        // 初始化位置指针
        this.positionIndexMap = new Map();
        this.vehicleIdList.forEach(vehicleId => {
            this.positionIndexMap.set(vehicleId, 0)
        })

        // 车辆状态信息
        this.vehicleStatusMap = new Map();
        this.vehicleIdList.forEach(vehicleId => {
            this.vehicleStatusMap.set(vehicleId, VehicleStory.VEHICLE_STATUS_RUNNING)
        })
    }

    getMessage() {
        return this.vehicleInfoList.map(({ id: vehicleId, type: vehicleType, modelUrl, plateNumber, thumbnailUrl }) => {
            const index = this.positionIndexMap.get(vehicleId);
            const positions = this.vehiclePositionMap.get(vehicleId);
            const status = this.vehicleStatusMap.get(vehicleId);
            const message = {
                vehicleId,
                vehicleType,
                modelUrl,
                plateNumber,
                thumbnailUrl,
                position: positions[index],
                status: status === VehicleStory.VEHICLE_STATUS_ACCIDENT ? VehicleStory.VEHICLE_STATUS_ACCIDENT : VehicleStory.VEHICLE_STATUS_RUNNING
            }
            if (status === VehicleStory.VEHICLE_STATUS_RUNNING) {
                this.positionIndexMap.set(vehicleId, index + 1);
                if (index >= positions.length - 1) {
                    this.positionIndexMap.set(vehicleId, 0);
                } else {
                    this.positionIndexMap.set(vehicleId, index + 1);
                }
            }
            return message;
        })
    }

    stopVehicle(vehicleId) {
        this.vehicleStatusMap.set(vehicleId, VehicleStory.VEHICLE_STATUS_STOP);
    }

    activeVehicle(vehicleId) {
        this.vehicleStatusMap.set(vehicleId, VehicleStory.VEHICLE_STATUS_RUNNING);
    }

    brokeVehicle(vehicleId) {
        this.vehicleStatusMap.set(vehicleId, VehicleStory.VEHICLE_STATUS_ACCIDENT);
    }
}
